let arr = [
  [1, 2, 3, 4, 5],
  [1, 3, 1],
  [4, 4, 4, 2, 1, 5],
];

let res1 = arr.reduce((s1, a) => {
  return (s1 =
    s1 +
    a.reduce((s2, el) => {
      return (s2 = s2 + el);
    }));
}, 0);

console.log(res1);

